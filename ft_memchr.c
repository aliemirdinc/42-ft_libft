#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*buffer;

	i = 0;
	buffer = (unsigned char *)s;
	while (i < n)
	{
		if (*buffer == (unsigned char)c)
			return (buffer);
		i++;
		buffer++;
	}
	return (0);
}
